    public void setTime() {

        new Thread(new Runnable() {
            @Override
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(1000);

                    } catch (InterruptedException ex) {
                        Logger.getLogger(OrderFrame.class.getName()).log(Level.SEVERE, null, ex);
                        System.out.println(".run()");
                    }

                    date = new Date();
                    tf = new SimpleDateFormat("h:mm:ss aa");
                    df = new SimpleDateFormat("EEEE,dd-MM-yyyy");
                    String time = tf.format(date);

                    // txtTime.setText(time.split(" ")[0]+" "+time.split(" ")[1]);
                    txtdate.setText(time.split(" ")[0] + " " + time.split(" ")[1] + "   " + df.format(date));

                }
            }
        }).start();
    }

