    public void showorderDetails() 
    {
        OrderTextArea.setText("");
      
       orderSubTotalValue=0.0F;
        orderTaxValue=0.0F;
         orderTotalValue=0.0F;
        
        if (orderedItemList.size() != 0) {
            OrderTextArea.setText("*************************  Cafe Order Details  **************************\n  " + "Time:-" + tf.format(date) + "\t\tDate:-" + df.format(date) + " \n");
            OrderTextArea.append("Customer Full Name:-" + selectedCustomer.getCustomerFullNAme() + "\tMob Number:-" + selectedCustomer.getMobNumber() + " \n"
                    + "********************************************************************\n");
            OrderTextArea.append("\tItem Name\t\t\tQuantity\tPrice(Rs)\t Total Price(Rs)"
            +"\n----------------------------------------------------------------------------------------------------\n");

            int itemcount = 0;
            for (OrderedItem item : orderedItemList) {
                itemcount++;
                StringBuilder sb = new StringBuilder(item.getItemName()); 

              orderSubTotalValue=orderSubTotalValue+(item.getQuantity()*item.getPrice());
              orderTaxValue=orderTaxValue+(item.getPrice()*(item.getTax()/100));
                OrderTextArea.append("\n"+ itemcount + "." +sb);
                OrderTextArea.append("\t\t\t "+item.getQuantity());
                OrderTextArea.append("\t" + item.getPrice());
                OrderTextArea.append("\t" + item.getQuantity() * item.getPrice());
              
            }
            
            
        }
     orderTotalValue=orderSubTotalValue+orderTaxValue;
     SubTotal.setText(""+orderSubTotalValue);
     Total.setText(""+orderTotalValue);
     this.orderTax.setText(""+orderTaxValue);
    }
